console.log(typeof(parseInt('15px', 10))); // получил: number

// МЕТОД to Boolean
// 1) 0; '' - пустая строка; null - не существует; undefined - есть, но не определен; NaN - Not a Number => превращаются в FALSE!!!
let switcher = null; // присвоим переменной switcher значение FALSE
if (!switcher) { // если значение не FALSE, то работает =>
	console.log('Working...');  // получил: Working...!!!
}
switcher = 1; // присвоим переменной switcher значение TRUE, так как это 1!!!
if (switcher) { // если значение TRUE, то работает =>
	console.log('Working...');  // получил: Working...!!!
}

