// МЕТОД to Number
// 1)
console.log(typeof(Number('4'))); // получил: number, так как метод Number() переводит в числовой данный тип данных, но им пользуются редко! 

// 2)
console.log(typeof(+'5')); // получил: number, применяется унарный плюс, РАСПРОСТРАНЕННЫЙ МЕТОД! 

// 3)
console.log(parseInt('15px', 10)); // получил: 15, метод parseInt() переводит в иное счисление строку
