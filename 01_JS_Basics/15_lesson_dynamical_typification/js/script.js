// 1)
console.log(typeof(String(null))); // получил: string, так как метод String() переводит в строковый данный тип данных
console.log(String(null)); // получил: null, так как метод String() переводит в строковый данный тип данных
console.log(typeof(String(4))); // получил: string, так как метод String() переводит в строковый данный тип данных

// 2)
console.log(typeof(5 + '')); // получил:  string, ПРИ СЛОЖЕНИИ ЦИФР СО СТРОКОЙ ПОЛУЧАЕМ СТРОКУ!!!

const num = 5;
